// Multiplication by 2 over GF(2**8)
u8 M(u8 x) {
  u8 t = 0;
  x = (x<<1) | (x>>7); // ROL by 1
  t += x&1;
  if (t) x ^= 0x1a;
  t -= x&1;
  return x;
}

u8 Multiply0x2(u8 x)
{
  return (M(x));
}

u8 Multiply0x3(u8 x)
{
  return (x ^ M(x));
}

u8 Multiply0xe(u8 x)
{
  return (M(x) ^ M(M(x)) ^ M(M(M(x))));
}

u8 Multiply0xb(u8 x)
{
  return (x ^ M(x) ^ M(M(M(x))));
}

u8 Multiply0xd(u8 x)
{
  return (x ^ M(M(x)) ^ M(M(M(x))));
}

u8 Multiply0x9(u8 x)
{
  return (x ^ M(M(M(x))));
}

